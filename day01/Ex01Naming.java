// 주석(Comment)
// 주석이란 프로그램 코드에 포함이 되지 않는
// 일종의 설명을 적는 방법이다.
// 주석은 한줄 주석과 여러줄 주석이 있는데
// 한줄 주석은 // 2개로 제일 앞에 적어주면 된다.
// 여러줄 주석은 /* */ 사이에 들어가 있는 모든 줄이
// 주석이 된다.
/*
 * 요렇게
 */

// 자바의 이름짓는 규칙
// 자바는 대소문자를 까다롭게 확인하기 때문에
// 대소문자가 틀리면 다른 것이 된다.
// 공통적인 규칙
// 1. 똑같은 이름은 한 { } 안에서는 만들수 없다.
// 2. 자바는 낙타등 표기법(Camel back notation)을 따라간다.
//    낙타등 표기법이란 여러 단어로 되어있을 경우 두번째 단어부터는
//    각 단어의 첫글자를 대문자로 적어주는 방법을 낙타등 표기법이라고 한다.
//    예시: FrontController, myNumber 등등
// 3. 자바에서 미리 사용하겠다고 한 예약어(keyword) 36가지가 있는데
//    해당 단어들로는 이름을 지어줄 수 없다.
//    따라서 그 36가지를 외어주자! -> 메모장으로코딩하던 옛날 옛적 이야기
//    이클립스에서 자주색으로 출력되는 단어들이 그러한 예약어이기 때문에
//    해당 단어들을 피해주면 된다!
// 

// 클래스 이름 짓기
// 클래스는대문자로 시작하는 명사 이다.
// 예시: String, Scanner, Ex01Naming, FrontContreoller

// 변수 이름 짓기
