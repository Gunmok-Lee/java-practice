// 그 공간에 우리가 원하는 값을 넣거나 불러올 수 있는 공간을 변수라고 한다.
// "변하는 수"
// 변수는 소문자로 시작하는 명사 이다.
// 예시: number, height, frontController

// 메소드 이름 짓기
// 메소드란, 해당 클래스에 정의되어있는 기능을 메소드라고 한다.
// 메소드는 소문자로 시작하는 동사 이다.
// 메소드를 가장 쉽게 알아차리는 방법은
// ()가 붙으면 무조건 메소드이다!
// 예시:print(), println(), printf(), select()

// 상수 이름 짓기
// 상수란 변수처럼 공간에 값을 넣지만 한번 넣는 순간 더이상 변경을 할 수 없는
// 공간을 상수라고 한다.
// 상수를 이름 지을 때에는
// 모든 글자를 대문자로 적어준다.
// 여러 단어일 경우에는 단어 사이에 _ 를 넣어두면 된다.
// 예시: PI, UNIT_PRICE

